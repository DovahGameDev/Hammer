function Player(name, maxHp, currentHp, strength, dexterity, intelligence, agility) {
    this.playerName = name;
    this.playerMaxHealth = maxHp;
    this.playerHealth = currentHp;
    this.playerStr = strength;
    this.playerDex = dexterity;
    this.playerInt = intelligence;
    this.playerAgl = agility;
}
